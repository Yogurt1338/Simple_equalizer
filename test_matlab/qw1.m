% Заданные параметры
fs = 44100; % Частота дискретизации (samples per second)
duration = 1; % Продолжительность каждого сигнала в секундах

% Время
t = 0:1/fs:duration-1/fs;

% Создание трех синусоидальных сигналов
y1 = 0.5 * sin(2 * pi * 2000 * t) + 1 * sin(2 * pi * 12000 * t); % Первые синусоиды
y2 = 0.2 * sin(2 * pi * 5000 * t); % Вторая синусоида с амплитудой 0.2 и частотой 5 кГц
y3 = 0.3 * sin(2 * pi * 15000 * t); % Третья синусоида с амплитудой 0.3 и частотой 15 кГц

% Объединение сигналов в один массив
y = [y1, y2, y3];

% Нормализация сигнала для предотвращения клиппинга
y = y / max(abs(y));

% Запись в аудиофайл
audiowrite('three_sinusoids.mp3', y, fs);
